package entity;

public class Sarcophagus {
    private int height;
    private int width;
    private int hideZoneWidth;
    private int hideZoneHeight;
    private double radius;

    public Sarcophagus() {
    }

    public int getHeight() {
        return height;
    }

    public void setHeight(int height) {
        this.height = height;
    }

    public int getWidth() {
        return width;
    }

    public void setWidth(int width) {
        this.width = width;
    }

    public int getHideZoneWidth() {
        return hideZoneWidth;
    }

    public void setHideZoneWidth(int hideZoneWidth) {
        this.hideZoneWidth = hideZoneWidth;
    }

    public double getRadius() {
        return radius;
    }

    public void setRadius(double radius) {
        this.radius = radius;
    }

    public int getHideZoneHeight() {
        return hideZoneHeight;
    }

    public void setHideZoneHeight(int hideZoneHeight) {
        this.hideZoneHeight = hideZoneHeight;
    }
}
